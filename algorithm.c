unsigned int Pad(unsigned char* pt, unsigned int length){

	unsigned int p, q, i;
	p = length / 32;
	q = (length % 32)/2;
	if (q == 0){
		strcat(pt, "80000000000000000000000000000000");
	}
	else if(q != 0){
		strcat(pt, "80");
		q = 16 - q - 1;
		for (i = 0; i < q; i++){
			strcat(pt, "00");
		}
	}
	length = strlen(pt);

	return length;

}



